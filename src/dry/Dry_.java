package dry;

public class Dry_ {
	private int dry_time;
	private String n;
	private boolean on = true ;
	private boolean off = false ;
	private int time;
	public int getDry_time() {
		return dry_time;
	}
	public void setDry_time(int dry_time) {
		this.dry_time = dry_time;
	}
	public boolean getOn() {
		return on;
	}
	public boolean getOff() {
		return off;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public String getN() {
		return n;
	}
	public void setN(String n) {
		this.n = n;
	}
	
}
